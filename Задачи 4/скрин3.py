def power(base, exponent):
    # базовый случай: если показатель равен 0, результат всегда равен 1
    if exponent == 0:
        return 1
    # рекурсивный случай: если показатель четный
    elif exponent % 2 == 0:
        # разбиваем задачу на меньшие части и рекурсивно вызываем функцию
        # для уменьшения количества умножений
        temp = power(base, exponent // 2)
        # Возводим в квадрат
        return temp * temp
    # рекурсивный случай: если показатель нечетный
    else:
        # уменьшаем показатель на 1 и рекурсивно вызываем функцию
        # затем умножаем результат на основание
        return base * power(base, exponent - 1)

# пример использования
base = 2
exponent = 10
result = power(base, exponent)
print(f"{base} в степени {exponent} равно {result}")
